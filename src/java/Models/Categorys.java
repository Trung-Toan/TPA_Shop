package Models;

/**
 * create an category this can is "quan ao vay ..."
 * 
 * @author trantoan
 */
public class Categorys {
    
    private int id;
    private String name;

    public Categorys() {
    }

    public Categorys(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
